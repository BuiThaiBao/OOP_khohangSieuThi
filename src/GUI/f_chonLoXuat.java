    public void buildJcombobox(){
        String sql = "SELECT id_lo FROM lo_san_pham where so_luong_sp!=-1;";
        accessDB.getInstance().open();
        ResultSet rs = accessDB.getInstance().excuteQuery(sql);
        try {
            while (rs.next()) {
                jComboBoxId_lo.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        setTextField();
        
    }
    public void setTextField(){
        int id_lo;
            if((String)jComboBoxId_lo.getSelectedItem()!=null){
                id_lo = Integer.parseInt((String) jComboBoxId_lo.getSelectedItem());
            }
            else{
                JOptionPane.showMessageDialog(null, "Trong kho không còn lô hàng nào !!!");
                jButton1.setEnabled(false);
                dispose();
                return ;
            }
        String sql="select so_luong_sp, ten_sp,id_kv from lo_san_pham join san_pham on lo_san_pham.ID_SP=san_pham.ID_SP where id_lo="+id_lo+";";
        ResultSet rs=accessDB.getInstance().excuteQuery(sql);
        try {
            if(rs.next()){
                jTextsoluong.setText(Integer.toString(rs.getInt(1)));
                jTexttenSP.setText(rs.getString(2));
                jTextField1.setText(rs.getString(3));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
