        // TODO add your handling code here:
        int[] selectedRows = jTable_TraCC.getSelectedRows();

        if (selectedRows.length > 0) {
            // Xóa các dòng từ model của JTable_TraCC
            DefaultTableModel model = (DefaultTableModel) jTable_TraCC.getModel();
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                int id = (int) model.getValueAt(selectedRows[i], 0); 
                String sql = "DELETE FROM phieu_tra_hang WHERE ID_Tra = " + id;
                accessDB.getInstance().executeUpdate(sql);
                model.removeRow(selectedRows[i]);
            }
        }
